package org.foobarspam.bicicleta;

public class Bicicleta {
	//Propiedades 
	private int id = 0;
	
	//Constructores
	public Bicicleta(){
		
	}

	public Bicicleta(int id) {
		this.id = id;
	}
	//Getters y Setters

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}
	
}	
